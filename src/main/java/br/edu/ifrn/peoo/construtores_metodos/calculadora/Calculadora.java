package br.edu.ifrn.peoo.construtores_metodos.calculadora;

public class Calculadora {

    public static double soma(double s1, double s2) {
        return s1 + s2;
    }

    public static double subtracao(double s1, double s2) {
        return s1 - s2;
    }

    public static double multiplicacao(double m1, double m2) {
        return m1 * m2;
    }

    public static double divisao(double d1, double d2) {
        return d1 / d2;
    }

}
